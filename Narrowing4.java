//narrowing for int 

class Narrowing4 {
    public static void main(String[] args){
        {
            char c = (char) 45;
            System.out.println("int to char " + c);

        }
        {
            short s = (short)728;
            System.out.println("int to short " + s);
        }
        {
            byte b = (byte)122;
            System.out.println("int to byte " + b);
        }
        
    }
    
}
